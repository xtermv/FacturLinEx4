#DROP TABLE eans;
CREATE TABLE IF NOT EXISTS eans
	(
	EAN0 CHAR(13) NOT NULL,
	EAN1 CHAR(13) NOT NULL,
	EAN2 CHAR(50),
	EAN3 DOUBLE(10,4) NOT NULL DEFAULT 0.00,
	EAN4 DOUBLE(10,2) NOT NULL DEFAULT 0.00,
	EAN5 DOUBLE(10,5) NOT NULL DEFAULT 0.00000,
	PRIMARY KEY kean (EAN0,EAN1),
        UNIQUE (EAN0,EAN1),
	INDEX kean (EAN0,EAN1)
	) ENGINE=InnoDB DEFAULT CHARSET=utf8;
