#DROP TABLE histoclie;
CREATE TABLE histoclie
	(
	HC0 INT(11) NOT NULL DEFAULT 0,
	HC1 DATE NOT NULL,
	HC2 TIME NOT NULL,
	HC3 INT(5) NOT NULL,
	HC4 CHAR(13),
	HC5 CHAR(50),
	HC6 DOUBLE(10,3) DEFAULT 0.000,
	HC7 DOUBLE(10,2) DEFAULT 0.00,
	HC8 CHAR(3),
	HC9 INT(7) NOT NULL,
	PRIMARY KEY khc (HC0,HC1,HC2,HC3),
        UNIQUE (HC0,HC1,HC2,HC3),
	INDEX khc (HC0,HC1,HC2,HC3)
	) ENGINE=InnoDB DEFAULT CHARSET=utf8;
